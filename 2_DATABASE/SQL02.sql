--7.
SELECT EXTRACT(YEAR FROM TO_DATE('99/10/11','YY/MM/DD')),TO_DATE('49/10/11','YY/MM/DD')
FROM DUAL;

--8.
SELECT STUDENT_NO, STUDENT_NAME
FROM TB_STUDENT
WHERE SUBSTR(STUDENT_NO,1,1)!='A';

--9.
SELECT ROUND(AVG(POINT),1)
FROM TB_GRADE
WHERE STUDENT_NO = 'A517178';

--10.
SELECT DEPARTMENT_NO AS "학과번호",COUNT(DEPARTMENT_NO) AS "학생수(명)"
FROM TB_STUDENT
GROUP BY DEPARTMENT_NO
ORDER BY DEPARTMENT_NO;

--11.
SELECT COUNT(*)
FROM TB_STUDENT
WHERE COACH_PROFESSOR_NO IS NULL;

--12.
SELECT TO_NUMBER(SUBSTR(TERM_NO,1,4)) AS "년도",ROUND(AVG(POINT),1) AS "년도 별 평점"
FROM (SELECT *
    FROM TB_GRADE
    WHERE STUDENT_NO = 'A112113')
GROUP BY SUBSTR(TERM_NO,1,4)
ORDER BY TO_NUMBER(SUBSTR(TERM_NO,1,4));

--13.
SELECT DEPARTMENT_NO
FROM TB_STUDENT
WHERE ABSENCE_YN = 'Y'
GROUP BY DEPARTMENT_NO
ORDER BY DEPARTMENT_NO;

--14.
SELECT STUDENT_NAME,COUNT(STUDENT_NAME)
FROM TB_STUDENT
GROUP BY STUDENT_NAME
HAVING COUNT(STUDENT_NAME)>=2;

--15.
SELECT SUBSTR(TERM_NO,1,4),SUBSTR(TERM_NO,5,2),POINT
FROM TB_GRADE
WHERE STUDENT_NO = 'A112113';