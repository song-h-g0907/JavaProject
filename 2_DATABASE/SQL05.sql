--3.
CREATE TABLE TB_국어국문학과
AS(SELECT STUDENT_NAME AS "학번",STUDENT_NAME AS "학생이름",EXTRACT(YEAR FROM TO_DATE(SUBSTR(STUDENT_SSN,1,6))) AS "출생년도",PROFESSOR_NAME AS "교수이름"
   FROM TB_STUDENT
   JOIN TB_PROFESSOR ON (PROFESSOR_NO = COACH_PROFESSOR_NO));

